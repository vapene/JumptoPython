# 튜플 슬라이싱
t1 = (1, 2, 'a', 'b')
t1[1:] # (2, 'a', 'b')

# 튜플 더하기
t2 = (3, 4)
t1 + t2 # (1, 2, 'a', 'b', 3, 4)

# 튜플 곱하기
t2 * 3 # (3, 4, 3, 4, 3, 4)
